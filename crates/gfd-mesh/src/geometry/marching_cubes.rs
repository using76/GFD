//! Marching Cubes algorithm for converting SDFs to triangle meshes.
//!
//! Implements the standard Marching Cubes with a 256-case lookup table.

use crate::geometry::distance_field::Triangle;

/// Convert an SDF to a triangle mesh using Marching Cubes.
pub fn marching_cubes(
    sdf: &dyn Fn([f64;3]) -> f64,
    bbox: [[f64;3];2],
    resolution: usize,
) -> Vec<Triangle> {
    if resolution == 0 { return Vec::new(); }
    let (nx,ny,nz) = (resolution, resolution, resolution);
    let dx = (bbox[1][0]-bbox[0][0])/nx as f64;
    let dy = (bbox[1][1]-bbox[0][1])/ny as f64;
    let dz = (bbox[1][2]-bbox[0][2])/nz as f64;
    let (nvx,nvy,nvz) = (nx+1, ny+1, nz+1);
    let total = nvx*nvy*nvz;
    let mut values = vec![0.0f64; total];
    for i in 0..nvx { for j in 0..nvy { for k in 0..nvz {
        let x = bbox[0][0]+i as f64*dx;
        let y = bbox[0][1]+j as f64*dy;
        let z = bbox[0][2]+k as f64*dz;
        values[i*nvy*nvz+j*nvz+k] = sdf([x,y,z]);
    }}}
    let mut triangles = Vec::new();
    for i in 0..nx { for j in 0..ny { for k in 0..nz {
        let ci: [(usize,usize,usize);8] = [
            (i,j,k),(i+1,j,k),(i+1,j+1,k),(i,j+1,k),
            (i,j,k+1),(i+1,j,k+1),(i+1,j+1,k+1),(i,j+1,k+1),
        ];
        let cp: [[f64;3];8] = [
            [bbox[0][0]+i as f64*dx, bbox[0][1]+j as f64*dy, bbox[0][2]+k as f64*dz],
            [bbox[0][0]+(i+1) as f64*dx, bbox[0][1]+j as f64*dy, bbox[0][2]+k as f64*dz],
            [bbox[0][0]+(i+1) as f64*dx, bbox[0][1]+(j+1) as f64*dy, bbox[0][2]+k as f64*dz],
            [bbox[0][0]+i as f64*dx, bbox[0][1]+(j+1) as f64*dy, bbox[0][2]+k as f64*dz],
            [bbox[0][0]+i as f64*dx, bbox[0][1]+j as f64*dy, bbox[0][2]+(k+1) as f64*dz],
            [bbox[0][0]+(i+1) as f64*dx, bbox[0][1]+j as f64*dy, bbox[0][2]+(k+1) as f64*dz],
            [bbox[0][0]+(i+1) as f64*dx, bbox[0][1]+(j+1) as f64*dy, bbox[0][2]+(k+1) as f64*dz],
            [bbox[0][0]+i as f64*dx, bbox[0][1]+(j+1) as f64*dy, bbox[0][2]+(k+1) as f64*dz],
        ];
        let mut cv = [0.0f64;8];
        for c in 0..8 {
            let (a,b,d) = ci[c];
            cv[c] = values[a*nvy*nvz+b*nvz+d];
        }
        let mut cube_idx: u8 = 0;
        for c in 0..8 { if cv[c] < 0.0 { cube_idx |= 1 << c; } }
        if cube_idx == 0 || cube_idx == 255 { continue; }
        let ef = EDGE_TABLE[cube_idx as usize];
        if ef == 0 { continue; }
        let mut ev = [[0.0f64;3];12];
        for e in 0..12 {
            if ef & (1<<e) != 0 {
                let (c0,c1) = EDGE_ENDPOINTS[e];
                ev[e] = interp_vert(cp[c0], cp[c1], cv[c0], cv[c1]);
            }
        }
        let ti = &TRI_TABLE[cube_idx as usize];
        let mut t = 0;
        while t < ti.len() && ti[t] != -1 {
            triangles.push(Triangle::new(ev[ti[t] as usize], ev[ti[t+1] as usize], ev[ti[t+2] as usize]));
            t += 3;
        }
    }}}
    triangles
}

fn interp_vert(p0: [f64;3], p1: [f64;3], v0: f64, v1: f64) -> [f64;3] {
    if (v0-v1).abs() < 1e-30 { return p0; }
    let t = (v0/(v0-v1)).clamp(0.0, 1.0);
    [p0[0]+t*(p1[0]-p0[0]), p0[1]+t*(p1[1]-p0[1]), p0[2]+t*(p1[2]-p0[2])]
}

